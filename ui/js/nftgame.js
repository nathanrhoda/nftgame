$(function()
{    
    var NFTGame;
    var tokenId;
    var speed = 1;
    var fuel = 1;
    var lastRefuel = 1;
    var range = 1;

    $(document).ready(function () {
        console.log("Ready Set Go");
        $('#btnMint').click(function(){            
            NFTGame.tokenCounter
                .call(function (err, result) {                
                    tokenId = result.toString()
                    console.log(result.toString());
                    NFTGame.mint   
                        .sendTransaction(speed, fuel, lastRefuel, range, function (err, mintResult) {
                            NFTGame.tokenCounter
                                .call(function (err, result) {                                
                                    console.log('New Token Id: ', result.toString());
                                });                        
                        });                                         
                });
        });                 
        

        $('#btnRetrieve').click(function(){
            NFTGame.tokenCounter
            .call(function (err, result) {                                
                var tokenId = result-1;
                console.log('Token Id: ', tokenId);
                NFTGame.getCarDetails
                .call(tokenId, function (error, result) {
                    alert(result);                                            
                });    
            });                              
        });
    });

    // Connecting Metamask
    if(typeof(web3) === 'undefined') {
        console.log("Unable to find web3. Please run metamask");
    } else {
        console.log("Found injected web3");
        web3=new Web3(web3.currentProvider);
        ethereum.enable();
        
        if(web3.version.network != 4){
            console.log('Wrong network detected. Expected Rinkeby');
        } else {
            console.log("Connected to Rinkeby");            
            NFTGame = web3.eth.contract(abi).at(address);
        }
    }    
});



